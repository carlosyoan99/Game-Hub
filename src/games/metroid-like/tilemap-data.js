/**
 * Devuelve el color de fondo para una sala según el tipo de amenaza
 */
export function getRoomBgColors(roomId, bossPresent, miniBossPresent, boss2Present) {
  if (boss2Present) return ['#1a0a0a', '#2a0a0a', '#1a0a0a'];
  if (bossPresent) return ['#1a0a1a', '#2a0a2a', '#1a0a1a'];
  if (miniBossPresent) return ['#1a1a0a', '#2a2a0a', '#1a1a0a'];
  return ['#0a1a2a', '#0a2a1a', '#0a1a2a'];
}

